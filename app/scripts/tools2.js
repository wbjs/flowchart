		Obj_2.prototype.pointUp=function(){
			return {'left':this.left+this.width/2,'top':this.top};
		}
		Obj_2.prototype.pointDown=function(){
			return {'left':this.left+this.width/2,'top':this.top+this.height};
		}
		Obj_2.prototype.pointLeft=function(){
			return {'left':this.left,'top':this.top+this.height/2};
		}
		Obj_2.prototype.pointRight=function(){
			return {'left':this.left+this.width,'top':this.top+this.height/2};
		}
		Obj_2.prototype.path=function(){
			ctx.lineWidth = this.border;
			ctx.beginPath();
			ctx.moveTo(this.left+this.width/2,this.top);
			ctx.lineTo(this.left,this.top+this.height/2);
			ctx.lineTo(this.left+this.width/2,this.top+this.height);
			ctx.lineTo(this.left+this.width,this.top+this.height/2);
			ctx.lineTo(this.left+this.width/2,this.top);
			ctx.strokeStyle="green";
			ctx.stroke();
		}
